import turtle as t

wn=t.Screen()
wn.setup(600,500)
wn.bgcolor('white')

obj=t.Turtle()
obj.width(2)
obj.color('green')
obj.shape('circle')
obj.shapesize(1)

obj.penup()
obj.goto(30,0)
obj.pendown()
obj.fd(100)
obj.lt(90)
obj.fd(100)
obj.rt(90)
obj.penup()
obj.goto(30,90)
obj.pendown()
obj.setheading(270)
obj.fd(100)
obj.penup()
obj.goto(-20,0)
obj.goto(0,45)
obj.pendown()
obj.setheading(270)
obj.fd(50)
obj.rt(90)
obj.fd(100)
obj.rt(-90)
obj.lt(-20)
obj.fd(80)
obj.penup()
obj.goto(-90,60)
obj.pendown()
obj.begin_fill()
obj.circle(10)
obj.end_fill()
obj.penup()
obj.goto(-55,60)
obj.pendown()
obj.begin_fill()
obj.circle(10)
obj.end_fill()
obj.penup()
obj.goto(-55,30)
obj.rt(68)
obj.fd(180)
obj.pendown()
obj.lt(-150)
obj.fd(64)
obj.rt(75)
obj.fd(75)
obj.rt(135)
obj.fd(128)
obj.setheading(270)
obj.fd(70)
obj.lt(-90)
obj.fd(150)
obj.rt(90)
obj.fd(80)
obj.penup()
obj.goto(-180,-40)
obj.pendown()
obj.begin_fill()
obj.circle(10)
obj.end_fill()
